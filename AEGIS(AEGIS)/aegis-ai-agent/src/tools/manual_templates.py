def get_manual(event_type: str) -> str:
    """
    이벤트 유형에 해당하는 대응 매뉴얼을 반환합니다.

    Args:
        event_type: 이벤트 유형 (ASSAULT, SWOON, BURGLARY, VANDALISM, DUMP)

    Returns:
        대응 매뉴얼 텍스트. 해당 유형이 없으면 기본 안내 메시지 반환.
    """
    return MANUAL_TEMPLATES.get(
        event_type,
        f"## 대응 매뉴얼\n{event_type}에 대한 매뉴얼이 등록되지 않았습니다.\n\n관리자에게 문의하세요."
    )


def list_event_types() -> list:
    """
    등록된 모든 이벤트 유형 목록을 반환합니다.

    Returns:
        이벤트 유형 리스트 ["ASSAULT", "SWOON", "BURGLARY", "VANDALISM", "DUMP"]
    """
    return list(MANUAL_TEMPLATES.keys())

