def is_future_effective(snippet):
    if pd.isna(snippet):
        return False
    m = re.search(r"\[시행일:\s*(\d{4})\.\s*(\d{1,2})\.\s*(\d{1,2})", str(snippet))
    if m:
        try:
            return date(*map(int, m.groups())) > TODAY
        except ValueError:
            return False
    return False


def get_category_rule(query):
    if pd.isna(query):
        return None
    for pattern, o_set, x_set in CATEGORY_RULES:
        if re.search(pattern, str(query)):
            return o_set, x_set
    return None


def auto_mark(row):
    if row["match"] == "정답":
        return "O", ""
    if row["match"] == "MISS":
        return "", "정답 유지"
    if pd.isna(row.get("returned_article")) or row.get("returned_article") == "":
        return "", "검토 필요"

    # 1. 미시행 법령
    if is_future_effective(row.get("snippet", "")):
        return "X", "미시행 - 자동 제외"

    # 2. expected에 직접 매칭
    expected = str(row.get("expected_articles", ""))
    expected_set = {a.strip() for a in expected.split("/")}
    if row["returned_article"] in expected_set:
        return "O", "정답에 추가"

    # 3. 카테고리 규칙
    rule = get_category_rule(row["query"])
    if rule:
        o_set, x_set = rule
        if row["returned_article"] in o_set:
            return "O", "정답에 추가"
        if row["returned_article"] in x_set:
            return "X", "무관"

    # 4. 매칭 실패
    return "", "검토 필요"


def main():
    df = pd.read_csv(INPUT, encoding="utf-8-sig")
    print(f"[1] 입력: {len(df)}행, {df['query'].nunique()}개 쿼리")

    # 빈 verdict만 처리
    blank_mask = df["human_verdict"].fillna("").eq("") & (df["match"] != "정답")
    print(f"[2] 처리 대상: {blank_mask.sum()}행")

    results = df[blank_mask].apply(auto_mark, axis=1, result_type="expand")
    df.loc[blank_mask, "human_verdict"] = results[0].values
    df.loc[blank_mask, "action"] = results[1].values

    # 통계
    auto_done = (df.loc[blank_mask, "human_verdict"] != "").sum()
    remaining = blank_mask.sum() - auto_done
    counts = df.loc[blank_mask, "action"].value_counts()

    print(f"\n[3] 결과:")
    for action, cnt in counts.items():
        print(f"    {action:25s}: {cnt}")
    print(f"    {'---':25s}---")
    print(f"    {'자동 완료':25s}: {auto_done} ({auto_done/blank_mask.sum()*100:.1f}%)")
    print(f"    {'검토 필요':25s}: {remaining}")

    # F1 재계산
    from collections import defaultdict
    query_groups = defaultdict(list)
    for _, r in df.iterrows():
        query_groups[r["query"]].append(r)

    f1s = {}
    for q, group in query_groups.items():
        correct = set()
        for r in group:
            if r.get("human_verdict") == "O" and pd.notna(r.get("returned_article")) and r["returned_article"]:
                correct.add(r["returned_article"])
            if r.get("match") == "MISS" and "유지" in str(r.get("action", "")):
                m = re.search(r"기대 조문:\s*(\S+)", str(r.get("snippet", "")))
                if m:
                    correct.add(m.group(1))

        all_ret = set()
        for r in group:
            if pd.notna(r.get("returned_article")) and r["returned_article"] and r.get("match") != "MISS":
                all_ret.add(r["returned_article"])

        if not correct or not all_ret:
            f1s[q] = 0.0
            continue
        tp = len(correct & all_ret)
        p = tp / len(all_ret)
        r_val = tp / len(correct)
        f1s[q] = 2 * p * r_val / (p + r_val) if (p + r_val) else 0.0

    macro = sum(f1s.values()) / len(f1s) if f1s else 0
    zeros = sum(1 for f in f1s.values() if f == 0)

    print(f"\n[4] Macro F1: {macro:.3f}")
    print(f"    F1=0: {zeros}/{len(f1s)}개")

    # 저장
    df.to_csv(OUTPUT, index=False, encoding="utf-8-sig")
